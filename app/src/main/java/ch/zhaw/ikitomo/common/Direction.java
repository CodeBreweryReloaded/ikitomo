package ch.zhaw.ikitomo.common;

/**
 * Represents nine direction for use with animations
 */
public enum Direction {
    /**
     * No direction/neutral
     */
    NONE,
    /**
     * North
     */
    UP,
    /**
     * North-East
     */
    UP_RIGHT,
    /**
     * East
     */
    RIGHT,
    /**
     * South-East
     */
    DOWN_RIGHT,
    /**
     * South
     */
    DOWN,
    /**
     * South-West
     */
    DOWN_LEFT,
    /**
     * West
     */
    LEFT,
    /**
     * North-West
     */
    UP_LEFT
}
