package ch.zhaw.ikitomo.common;

/**
 * Represents the state of an ikitomo.
 */
public enum StateType {
    /**
     * The idle state
     */
    IDLE,
    /**
     * The walking state
     */
    RUN,
    /**
     * The sleeping state
     */
    SLEEP,
    /**
     * The wakening state
     */
    WAKE,
    /**
     * The yawning state
     */
    YAWN,
    /**
     * The eating state
     */
    EAT
}
