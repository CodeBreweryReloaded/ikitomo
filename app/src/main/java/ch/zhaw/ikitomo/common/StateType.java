package ch.zhaw.ikitomo.common;

/**
 * Represents the state of an ikitomo.
 */
public enum StateType {
    /**
     * The idle state
     */
    IDLE,
    /**
     * The thinking state
     */
    THINK,
    /**
     * The walking state
     */
    RUN,
    /**
     * The sleeping state
     */
    SLEEP,
    /**
     * The wakening state
     */
    AWAKE,
    /**
     * The yawning state
     */
    YAWN
}
